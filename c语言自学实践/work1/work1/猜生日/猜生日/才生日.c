#include <stdio.h>
int main()
{
	long int y, m, d,x;/*定义变量year，month，day*/
	for (y = 1912;y <= 2012;y++)/*逐年校对年份是否符合*/
	{
		for (d = 1;d <= 31;d++)/*逐日校对是否符合*/
		{
			x = y * 10000 + 6 * 100 + d;/*年是四位数故年乘以10000，六月出生则6*100，6月有31日故d取值为1―31*/
			if (x % 2012 == 0 && x % 12 == 0)/*根据题意，x要被2012整除，而12是3的倍数，只要x被12整除则必被3整除，因此x%3==0可以省略*/
			{
				d = 32;/*符合条件，将日赋值为32跳出循环*/
				
				printf("%d", x);
			}

		}
	}
}